# Ambil input untuk mengisi nilai
x = input("Masukkan nilai x: ")
y = input("Masukkan nilai y: ") 
z = input("Masukkan nilai z: ")

# Menggunakan operator perjumlahan
hasil= float(x) + float(y) + float(z)
print("Hasil penjumlahan x, y, z adalah = ", hasil)

# Menggunakan operator pengurangan
hasil= float(x) - float(y) - float(z)
print("Hasil pengurangan x, y, z adalah = ", hasil)

# Menggunakan operator pembagian
hasil= float(x) / float(y) / float(z)
print("Hasil pembagian x, y, z adalah = ", hasil)

# Menggunakan operator perkalian
hasil= float(x) * float(y) * float(z)
print("hasil perkalian = ",hasil)
